export const getTheme = (id) =>
  THEMES.find((t) => t.id === id) || THEMES[0];

// Build a style object of CSS vars to inline on the PublicPage root
export const buildThemeStyle = (id) => {
  const theme = getTheme(id);
  return theme.vars;
};

// Get QR code colors for a theme
export const getThemeQrColors = (id) => {
  const theme = getTheme(id);
  return theme.qr || { dark: "#c084fc", light: "#ffffff06" };
};